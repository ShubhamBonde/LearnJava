public class VariablesInJava {
    public static void main(String[] args) {

        // I'm defining variable called 'num' here.
        int num = 0;
      
        System.out.println(num);
    }

    /**
     * Notes:
     * - Variables in java are case sensitive
     * - Variables cannot start with a number. You can use $, _, a-z, A-Z
     * - Subsequent characters in the variable identifier can contain alphabets, digits, $ or _
     * - Reserved keywords in Java can't be used as variable identifier.
     * - Conventions: camelCase is used in java to name a variable
     * - Constants must be defined in capital letters with distinct words separated by _. Ex. MAX_LENGTH, CLIENT_SIDE_URL
     */
}
